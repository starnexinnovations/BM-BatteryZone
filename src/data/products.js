// Helper to get stock label & color
export function getStockInfo(status) {
  switch (status) {
    case 'in_stock':
      return { label: 'In Stock', color: 'text-green-500', bgColor: 'bg-green-500/10 border-green-500/20', dot: 'bg-green-500' };
    case 'low_stock':
      return { label: 'Limited Stock', color: 'text-amber-500', bgColor: 'bg-amber-500/10 border-amber-500/20', dot: 'bg-amber-500' };
    case 'out_of_stock':
      return { label: 'Contact for Availability', color: 'text-red-500', bgColor: 'bg-red-500/10 border-red-500/20', dot: 'bg-red-500' };
    default:
      return { label: 'Available', color: 'text-green-500', bgColor: 'bg-green-500/10 border-green-500/20', dot: 'bg-green-500' };
  }
}
